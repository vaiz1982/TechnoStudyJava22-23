package unit04.iteration.day19;

public class NestedLoopReview {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            System.out.println("Outer");
            for (int j = 0; j < 5; j++) {
                System.out.print ("Inner");
            }
        }
    }
}
